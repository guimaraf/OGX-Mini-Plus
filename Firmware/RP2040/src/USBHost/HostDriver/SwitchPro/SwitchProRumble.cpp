    constexpr uint8_t AMP_HIGH = 0x88;
    constexpr uint8_t AMP_LOW_LSB = 0x62;
    constexpr uint8_t DEFAULT_HIGH_FREQ = 0x01;
    constexpr uint8_t DEFAULT_LOW_FREQ = 0x40;
    constexpr uint8_t DEFAULT_AMP = 0x40;

    constexpr const RumbleFreqData& find_rumble_freq(uint16_t freq)
    {
        size_t idx = 0;
        if (freq > RUMBLE_FREQS.front().freq)
        {
            for (size_t i = 1; i < RUMBLE_FREQS.size() - 1; ++i)
            {
                if (freq > RUMBLE_FREQS[i - 1].freq && freq <= RUMBLE_FREQS[i].freq)
                {
                    idx = i;
                    break;
                }
            }
        }

        return RUMBLE_FREQS[idx];
    }
