namespace SwitchProRumble
{
    void fill_neutral(uint8_t (&data)[4])
    {
        data[0] = 0x00;
        data[1] = DEFAULT_HIGH_FREQ;
        data[2] = DEFAULT_LOW_FREQ;
        data[3] = DEFAULT_AMP;
    }

    void encode(uint8_t (&data)[4], uint8_t magnitude)
    {
        if (magnitude == 0)
        {
            fill_neutral(data);
            return;
        }

        const auto& low_freq = find_rumble_freq(static_cast<uint16_t>(magnitude) << 2);
        const auto& high_freq = find_rumble_freq(magnitude);

        data[0] = static_cast<uint8_t>((high_freq.high >> 8) & 0xFF);
        data[1] = static_cast<uint8_t>((high_freq.high & 0xFF) + AMP_HIGH);
        data[2] = low_freq.low;
        data[3] = AMP_LOW_LSB;
    }
}
